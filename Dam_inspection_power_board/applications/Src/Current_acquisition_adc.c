/*
 * adc_dma初始化程序，调用即可开始采集adc且通过代码搬运到全局变量中
 */
void current_acq_init(void)
{
    MX_DMA_Init();
    MX_ADC1_Init();
    HAL_ADC_Start_DMA(&hadc1,(uint32_t*) current_adc_data, (uint32_t)8);
    HAL_ADC_Start(&hadc1);
}

